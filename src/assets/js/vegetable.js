class Vegetable {
    constructor(vegetable) {
        this.id = vegetable.Id
        this.name = vegetable.Name
        this.variety = vegetable.Variety
        this.primaryColor = vegetable.PrimaryColor
        this.lifeTime = vegetable.LifeTime
        this.fresh = vegetable.Fresh
        this.price = vegetable.Price
    }
}

export {Vegetable}